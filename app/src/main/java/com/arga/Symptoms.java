package com.arga;

public class Symptoms {
    private String symptomps;
    private String symptomDetails;
    private int symptomPictures;

    public String getSymptoms() {
        return symptomps;
    }

    public void setSymptoms(String symptomps) {
        this.symptomps = symptomps;
    }

    public String getSymptomDetails() {
        return symptomDetails;
    }

    public void setSymptomDetails(String symptomDetails) {
        this.symptomDetails = symptomDetails;
    }

    public int getSymptomPictures() {
        return symptomPictures;
    }

    public void setSymptomPictures(int symptomPictures) {
        this.symptomPictures = symptomPictures;
    }
}

